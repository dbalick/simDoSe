#!/bin/bash

## --------------------------------------------------
##  To create the simDoSe environment,
##      use the following command:
## --------------------------------------------------
conda create -n simdose python=2.7 numpy scipy matplotlib pandas



## --------------------------------------------------
##  To run simDoSe, first use the follwing command
##      to activate the `simdose' the environment
## --------------------------------------------------
# conda activate simdose



## --------------------------------------------------
##  When you are finished, use the following command
##      to deactivate the `simdose' the environment
## --------------------------------------------------
# conda deactivate



## --------------------------------------------------
##  If you try to run simDoSe and you find there is
##     a missing package, use the following commands
## to install that package in the simdose environment
## --------------------------------------------------
# conda activate simdose
# conda install <missing package>



